            <p class="text-white text-base mb-1"> A Death Dealer Union é uma banda americana de metal alternativo e gótico formada em Los Angeles, Califórnia, no final de 2019. O projeto surgiu da colaboração entre o baterista CC McKenna e o guitarrista Doug Weiand, que buscavam criar uma sonoridade que mesclasse elementos do metal moderno com influências esotéricas e ocultistas. Em 2022, a banda ganhou destaque ao integrar a vocalista Lena Scissorhands (Elena Cataraga), conhecida por seu trabalho com a banda moldava Infected Rain. A entrada de Lena trouxe uma nova dinâmica ao grupo, consolidando sua formação e identidade sonora. ​


Principais Músicas
O álbum de estreia da banda, intitulado "Initiation", foi lançado em 22 de setembro de 2023. Este trabalho apresenta uma variedade de estilos dentro do metal, destacando-se as seguintes faixas:​
Femetalism

"Borderlines" e "Beneath The Surface": Singles lançados em 2022 que introduziram Lena como vocalista e receberam ampla aceitação do público. ​

"Ill Fated": Combina passagens de guitarra limpas inspiradas nos anos 80 com riffs metálicos intensos, evidenciando a versatilidade vocal de Lena.​

"Ekphrasis" e "Mythos": Faixas que homenageiam os riffs pesados do metal dos anos 90, incorporando nuances do nu-metal.​

"Love Me When I'm Ugly": Uma balada pesada e emotiva que destaca a profundidade lírica da banda.​

O álbum também inclui outras músicas notáveis, como "The Vow of Silence", "The Integument", "The Big Blue", "Back To Me", "The Downfall", "Anew" e "Beyond Heaven". ​
Femetalism

Influências
A Death Dealer Union incorpora uma ampla gama de influências musicais, desde bandas clássicas como Iron Maiden e Metallica até elementos do metal alternativo e gótico. A vocalista Lena Scissorhands é conhecida por sua habilidade em alternar entre vocais limpos e guturais, técnica que também utiliza em seu trabalho com o Infected Rain. Essa versatilidade vocal contribui para a identidade única da banda. ​


Formação Atual
Lena Scissorhands – Vocais

CC McKenna – Bateria

Doug Weiand – Guitarra solo

Hunter Havok – Guitarra base e solo

Jonny Heinz – Baixo

A banda assinou contrato mundial com a gravadora Napalm Records em 2022, marcando um passo significativo em sua carreira.

