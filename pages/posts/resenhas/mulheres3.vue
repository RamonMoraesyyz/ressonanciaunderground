            <p class="text-white text-base mb-1"> A Dogma é uma banda de metal melódico formada por quatro integrantes que se apresentam como freiras, envoltas em um manto de mistério e teatralidade. Com uma estética sombria e provocativa, elas combinam elementos visuais e musicais para transmitir mensagens de liberdade e questionamento de dogmas estabelecidos.​


Origem e Identidade
Embora a origem exata da Dogma seja envolta em mistério, há indícios de que a banda tenha sido concebida na América do Sul, possivelmente com integrantes brasileiras. O vídeo do single de estreia, "Father I Have Sinned", foi filmado na Catedral Metropolitana de Montevidéu, no Uruguai, e a conta oficial da banda no Instagram está registrada nesse país. ​

As integrantes adotam nomes simbólicos como Lilith, Lamia, Nixe e Abrahel, reforçando a aura mística e desafiadora da banda. Elas se apresentam com vestes de freiras e maquiagem inspirada no estilo "corpse paint", comum no black metal, criando uma imagem visual impactante que complementa sua proposta artística.​

Estilo Musical e Influências
Musicalmente, a Dogma transita entre o metal melódico, heavy metal clássico e elementos de power metal, com riffs pesados, vocais intensos e arranjos dramáticos. Suas letras abordam temas como libertação pessoal, crítica a instituições religiosas e sociais, e a busca por autenticidade.​

A banda é frequentemente comparada a grupos como Ghost, devido à sua combinação de teatralidade, simbolismo religioso e sonoridade envolvente. ​

Principais Músicas
Father I Have Sinned (2022): Single de estreia que apresenta a estética e temática provocativa da banda, explorando a dualidade entre pecado e redenção.​

My First Peak (2023): Faixa que continua a narrativa desafiadora da Dogma, com letras que exploram o desejo e a transgressão, reforçando sua identidade artística.


