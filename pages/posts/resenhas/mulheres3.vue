            <p class="text-white text-base mb-1"> A Dum Chica é uma banda argentina de punk alternativo formada em Buenos Aires em 2021. O grupo surgiu durante a pandemia com uma proposta ousada: um trio sem guitarras, composto por Lucila Storino (voz), Juana Inés Gallardo (baixo) e Ramiro Pampin (bateria). Inspiradas por bandas como The Velvet Underground, Iggy Pop & The Stooges e ESG, elas criaram um som cru e dançante, combinando proto-punk, dance punk e post-punk. ​


Estilo e Influências
O som da Dum Chica é marcado por linhas de baixo distorcidas, batidas marcantes e letras provocativas. A ausência de guitarras destaca a força do baixo e da bateria, criando uma atmosfera intensa e minimalista. Suas composições abordam temas como crítica social, política e experiências urbanas, refletindo uma postura contestadora e engajada.​

Discografia e Principais Músicas
Álbum "DUM" (2023): Gravado ao vivo para capturar a energia das apresentações, inclui faixas como "Veneno", "Ey Ey", "Hooky" e "Terremoto". ​

Álbum "Súper Premium Ultra" (2024): Lançado pela Virgin Records Argentina, este trabalho expandiu o som da banda com elementos eletrônicos e produção mais elaborada, destacando músicas como "Suerte" e "Supersónico". ​
Sopitas.com

Singles recentes: "El Hit" e "Such a Witch" (2024) e "Desconocido" (2025), este último lançado após a polêmica no Lollapalooza Argentina. ​


Polêmica no Lollapalooza Argentina 2025
Durante sua apresentação no festival, a Dum Chica exibiu um vídeo com imagens do presidente argentino Javier Milei com características demoníacas e a cabeça explodindo. A performance gerou controvérsia nas redes sociais, com críticas de apoiadores do presidente. A banda assumiu a responsabilidade pelo conteúdo e emitiu um comunicado isentando o festival e seus organizadores. ​

