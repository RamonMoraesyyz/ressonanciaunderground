                Embora jovem, tanto em idade quanto em trajetória, a Gastação Infinita já ostenta mais de 50 shows realizados em cinco anos, somados a quatro videoclipes lançados. Em 2022, o clipe Chave do Portão conquistou o prêmio de "Melhor clipe musical pelo júri popular" na IV Mostra Clube Colorado, acumulando mais de 3000 visualizações no YouTube. No ano seguinte, Cornélios elevou o patamar, somando mais de 5000 visualizações nas plataformas digitais e recebendo o prêmio de "Melhor videoclipe pelo Júri Popular" no 30º Festival de Cinema de Vitória.



