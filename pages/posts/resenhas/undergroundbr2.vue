                Em 2005, os Cartolas venceram o festival "Claro que é Rock", o que impulsionou sua carreira. Seu álbum de estreia, "Original de Fábrica" (2007), produzido por Carlos Eduardo Miranda na Toca do Bandido, no Rio de Janeiro, rendeu à banda o Prêmio Açorianos de Música na categoria Melhor Disco Pop. Desse trabalho, destacam-se faixas como "Cara de Vilão" e "Sujeito Boa Praça", que evidenciam a capacidade do grupo de mesclar letras sarcásticas com melodias cativantes.

