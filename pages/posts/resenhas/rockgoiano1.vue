            Seu álbum de estreia, As Plantas Que Curam (2013), lançado pela gravadora norte-americana Other Music, atraiu atenção mundial e colocou Goiânia no mapa do rock psicodélico moderno. Desde então, Boogarins lançou álbuns aclamados, como Manual (2015) e Sombrou Dúvida (2019), expandindo sua sonoridade e incorporando experimentações sonoras que vão do rock ao eletrônico, mantendo uma essência psicodélica vibrante.

            A banda não só inovou no som, mas também ajudou a fortalecer a conexão de Goiânia com o cenário internacional, sendo uma das poucas bandas goianas a fazer extensas turnês pela Europa e América do Norte. A influência do Boogarins no rock goiano é profunda, inspirando uma nova geração de músicos locais a explorar a psicodelia, a experimentação e a busca por uma estética sonora única.

