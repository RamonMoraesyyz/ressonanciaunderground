            <p class="text-white text-base mb-1"> MQN, formada em 1997 em Goiânia, é uma das bandas mais influentes da cena rock goiana, sendo pioneira no movimento de garage rock e stoner rock no Brasil. Com sua sonoridade crua e enérgica, fortemente influenciada por bandas como MC5, The Stooges e Black Sabbath, o grupo trouxe uma estética de rock visceral e agressivo, que rapidamente se destacou no cenário underground.

                A presença de palco intensa de Fabrício Nobre, vocalista e figura chave na cena musical local, ajudou a catapultar a banda para o reconhecimento nacional. Com discos como Hellburst e Badass Rock’n’Roll, MQN se consolidou como uma referência do rock independente, mantendo uma postura DIY e participando ativamente na organização de festivais como o Goiânia Noise, evento crucial para o desenvolvimento da cena rock da região.

                A influência do MQN para o rock goiano vai além de sua música. A banda foi fundamental na criação de uma infraestrutura para o rock independente em Goiânia, inspirando e abrindo caminho para outras bandas locais. Seu papel foi decisivo na consolidação da cidade como um polo de rock alternativo no Brasil, sendo até hoje lembrada como uma das forças propulsoras do "Goiânia Rock City".

