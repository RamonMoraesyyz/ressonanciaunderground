            <p class="text-white text-base mb-1"> Zulu não é apenas uma banda de powerviolence. É um projeto político-sônico, uma resposta visceral ao racismo estrutural e uma celebração da resistência negra. Nascidos em Los Angeles, liderados pelo multi-instrumentista Anaiah Lei, eles transformam a fúria do hardcore tradicional em algo mais denso e intencionalmente desafiador.

            Seu som é uma colisão calculada:

            A velocidade brutal do powerviolence;

            A atmosfera pesada do doom metal;

            Interlúdios de soul, jazz e spoken word que quebram a agressão como respiros profundos.

            Álbuns como "My People... Hold On" (2019) e, principalmente, "A New Tomorrow" (2023), são manifestos audíveis. As letras confrontam opressão, celebram identidade e homenageiam ícones negros (como em "Where I'm From", que sampleia James Baldwin). A violência rítmica não é vazia: é catarse coletiva, um lamento transformado em martelo.

            Ao vivo, Zulu é imersão e confronto. Anaiah alterna entre vocais guturais e discursos inflamados, enquanto samples de discursos históricos e melodias ancestrais criam pontes entre o caos e a memória. Eles não tocam para o público; convocam-no à reflexão ativa.

            Crucial: Zulu subverte a cena hardcore branca não por assimilação, mas por reivindicação radical de espaço. Sua música é um arquivo vivo de dor e potência, onde cada breakdown soa como quebra de corrente e cada interlúdio, um momento de resgate cultural.

            Em síntese: Zulu é mais que música — é arqueologia da luta negra em forma de distorção. Eles lembram que o hardcore pode ser ferramenta de descolonização sonora, onde a beleza existe mesmo no que é intencionalmente despedaçado. Um farol brutal e necessário.

            Ponto final: Enquanto muitos repetem fórmulas, Zulu escava o passado para forjar um novo amanhã. Literalmente.



