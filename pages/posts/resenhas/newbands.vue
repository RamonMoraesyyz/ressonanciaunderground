            <p class="text-white text-base mb-1"> Imagine a energia crua de um porão de hardcore colidindo com o refrão ensolarado de um hit de power-pop dos anos 90. É nesse território imprevisível que o Angel Du$t planta sua bandeira. Liderados pelo carismático e versátil Justice Tripp (ex-Trapped Under Ice, Turnstile), a banda de Baltimore é um experimento contínuo em desconstrução de gêneros.

                Longe de ser apenas um "side-project", o Angel Du$t evoluiu de raízes hardcore/punk para um som que beira o lofi-indie, o surf-rock desgastado e até baladinhas acústicas distorcidas. É como se Replacements, Descendents e um toque de Beck se encontrassem num estúdio improvisado, decidindo que melodias cativantes e letras introspectivas podem, sim, conviver com distorção e atitude.

                O que define?
                A contradição gostosa: músicos que parecem saídos de uma turnê metal tocando canções que soam como hinos adolescentes melancólicos. Tripp canta com uma entrega crua e quase despreocupada, misturando sussurros confessais com gritos esporádicos, sempre envolto em ganchos inesperadamente grudentos. A produção é deliberadamente crua, mantendo a urgência do punk, mas abraçando uma sensibilidade pop peculiar.

                Discografia Chave:

                "Rock the Fuck On Forever" (2016): O manifesto da virada, onde o hardcore começa a dar espaço ao pop.

            "Pretty Buff" (2019): O ápice da mistura, com faixas dançantes e letras que vão do absurdo ao profundamente pessoal.

            "YAK: A Collection of Truck Songs" (2021): Amadurecimento sonoro, experimentando com folk, grunge e ainda mais melodias inesperadas.

            
            Angel Du$t não é para puristas de gênero. É para quem busca autenticidade sem pose, melodias que pegam pelo contraste e uma energia que flutua entre a euforia e a melancolia suburbana. Eles são a prova de que você pode tirar o hardcore do porão, mas não tira a alma inquieta e inventiva da música. Uma banda que transforma o caos em algo estranhamente bonito e irresistível.

