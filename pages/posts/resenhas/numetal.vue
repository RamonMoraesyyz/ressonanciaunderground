            <p class="text-white text-base mb-1"> Nascida em Gotemburgo (Suécia) em 1993, a Headplate foi um experimento de nu metal cru com DNA escandinavo. Formada pelo baterista Niklas Österlund e o guitarrista Daniel Granstedt, carregava na essência uma filosofia livre: "criar a música que sentiam ser certa, sem limites de gênero".


            Marcaram presença no Hultsfred (2001) e Tuska Open Air (2001), abrindo para nomes como Machine Head e Hardcore Superstar – evidência de respeito na cena underground.

            Lançaram 4 álbuns oficiais: *Bullsized (2000), Delicate (2002), 15/8 (2002)* e Pieces (2003), todos com distribuição independente pela Gain Productions. Um quinto trabalho, *12-12-12* (2012), surgiu de demos abandonados em 2006, mas sem o vocalista original – e sem convencer fãs hardcore.


            "Hardcore metal com refrões melódicos, riffs pesados e vocais que alternam fúria e afinação" – descrição autoimposta que ecoava Korn, mas com frieza nórdica.

    
            Separaram-se em 2006, no ápice das gravações do 4º disco.

            Fãs os celebram como "os herdeiros europeus da velha escola nu metal", especialmente pelo álbum Bullsized. Todas as músicas estão disponíveis para download gratuito no site oficial – gesto raro que virou seu epitáfio digital.

            "Foram menos uma banda e mais um manifesto garageband: provaram que até na Suécia nevada o baixo 7 cordas ecoava, mesmo que só em festivais de inverno."

            Apesar do tom "Korn do Báltico", seu fim antecipado os transformou em relíquia de uma era onde nu metal era língua franca da rebeldia jovem – antes do metalcore dominar a cena.

