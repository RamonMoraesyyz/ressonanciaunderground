            <p class="text-white text-base mb-1">Mais do que uma banda, o Pleymo foi um experimento sociocultural com distorção. Nascido nos subúrbios de Paris no final dos anos 90, eles não seguiam o manual do Nu Metal americano: traziam uma irreverência tipicamente francesa, misturando riffs sísmicos de guitarra (afinadas em tons abissais) com batidas de hip-hop e até toques de raï argelino. Era metal com je ne sais quoi: agressividade disfarçada de festa caótica.

            A voz de Mark Maggiori – entre rap sincopado, gritos cortantes e melodias quase new wave – era a alma da contradição. Letras em francês e inglês falavam de alienação urbana, mas com um sarcasmo quase cartoonizado ("Blöhm" soava como um hino para desajustados dançantes). Visual? Uma explosão kitsch: cabelos coloridos, roupas cyberpunk e uma energia palco que lembrava um rave industrial.

            Álbuns como "Episode 2: Medecine Cake" (2001) são cápsulas do tempo: guitarras pesadas colidiam com sintetizadores digitais ("United Nowhere"), enquanto "Modigliani" mostrava sua ousadia ao incorporar violinos e samples orientais. O Pleymo nunca levou a si próprio a sério demais – e talvez esse fosse seu trunfo.

            Legado? Pioneiros na "French Touch" metalizada, provaram que a angústia nu-metal podia ter groove, cor e um croissant amassado no bolso. Desafiou purismos e deixou um rastro de caos criativo que ainda ecoa em bandas que ousam misturar genêros com gourmandise.

            "Eles eram o lado B psicodélico do movimento: menos baggy pants, mais arte de rua com amplificadores."

